//arredondamento

let numero = 4.7

//Math.round é um arredondamento comum

console.log(Math.round(numero))

//math.ceil é um arredondamento para cima

console.log(Math.ceil(numero))

//math.floor é um arredondamento para baixo

console.log(Math.floor(numero))

//math.pow gera potenciação

console.log(Math.pow(2, 3))

//Math.sqrt gera radiciação

console.log(Math.sqrt(35))

//Math.abs transforma tudo em positivo

console.log(Math.abs(-5))

//Math.min seleciona o menor número de uma sequência

console.log(Math.min(43, 25, 76, 10))

//math.max seleciona o maior número de uma sequência

console.log(Math.max(75, 22, 37, 89))